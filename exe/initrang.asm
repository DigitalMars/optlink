		INCLUDE	MACROS

		PUBLIC	INIT_FINAL_RANGES


		.DATA

		EXTERNDEF	MAX_FINAL_TABLE:DWORD,MAX_FINAL_RANGES:DWORD,FINAL_TABLE:DWORD,FINAL_RANGES:DWORD


		.CODE	PASS2_TEXT


INIT_FINAL_RANGES	PROC
		;
		;SET UP FINAL_TABLE AND FINAL_RANGES
		;

		MOV	ECX,OFF FINAL_TABLE+8
		MOV	EDX,OFF FINAL_RANGES+8

		XOR	EAX,EAX
		MOV	MAX_FINAL_TABLE,ECX

		MOV	[ECX-8],EAX
		DEC	EAX

		MOV	[ECX-4],EAX
		MOV	[EDX-8],EAX

		MOV	[EDX-4],EAX
		MOV	MAX_FINAL_RANGES,EDX

		RET

INIT_FINAL_RANGES	ENDP


		END

