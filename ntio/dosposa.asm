		TITLE	DOSPOSITION_A

		INCLUDE	MACROS
		INCLUDE	IO_STRUC

		PUBLIC	DOSPOSITION_A


		.CODE	ROOT_TEXT

DOSPOSITION_A	PROC
		;
		;EAX IS MYO_STRUCT
		;ECX IS DESIRED POSITION
		;
		;RETURNS EAX == MYO_STRUCT
		;
		ASSUME	EAX:PTR MYO_STRUCT

		OR	[EAX].MYO_SPEC_FLAGS,MASK F_SEEK_FIRST
		MOV	[EAX].MYO_DESPOT,ECX

		RET

DOSPOSITION_A	ENDP


		END

