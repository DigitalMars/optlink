		PUBLIC	SET_OUTFILE


		.DATA

		EXTERNDEF	_OUTFILE_GINDEX:DWORD,CURN_OUTFILE_GINDEX:DWORD

		EXTERNDEF	OUTFILE_GARRAY:STD_PTR_S


		.CODE	PASS2_TEXT


SET_OUTFILE	PROC
		;
		;EAX IS SECTION PHYSICAL
		;
		MOV	ECX,_OUTFILE_GINDEX

		MOV	[EAX].SECTION_STRUCT._SECT_OUTFILE_GINDEX,ECX

		CONVERT	ECX,ECX,OUTFILE_GARRAY

		INC	[ECX].OUTFILE_STRUCT._OF_SECTIONS

		RET

SET_OUTFILE	ENDP


		END

