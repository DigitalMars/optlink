		TITLE	ASLROVL

		INCLUDE	MACROS

if	any_overlays

		PUBLIC	ADD_SLROVL_TO_LIST

		.CODE	PASS1_TEXT

	SOFT	EXTP	LIB_ROUTINE_FINAL

		ASSUME	DS:NOTHING

ADD_SLROVL_TO_LIST	PROC
		;
		;IF DOING OVERLAYS, AND NOT ALREADY DONE, ADD SLROVL.LIB TO
		;LIBRARY LIST.
		;
		BITT	DOING_OVERLAYS
		JZ	9$
		BITT	SLROVL_ADDED
		JNZ	9$
		;
		;TREAT LIKE COMENT RECORD...
		;
		SETT	SLROVL_ADDED
		FIXDS
		FIXES
		LEA	SI,SLROVL_COMENT
		CALL	LIB_ROUTINE_FINAL
		CLC
		RET

9$:
		STC
		RET

ADD_SLROVL_TO_LIST	ENDP

		.DATA

if	fg_plink
		DW	7
SLROVL_COMENT	DB	'SLROVL',0DH,1AH
else
		DW	8
SLROVL_COMENT	DB	'OPTCOMP',0DH,1AH
endif

endif

		END

